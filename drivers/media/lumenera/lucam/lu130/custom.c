int CustomSoftFormatAdjustColorId(plucam_device pExt, int stillMode)
{
   struct usb_device *udev;
   int revId, prodId;
   __u32 colorId;
   int sixteenBitsMode;
   int useBinY;
   WORD binY;
   
   udev = pExt -> udev;
   if(!udev) return -EFAULT;

   revId = udev->descriptor.bcdDevice;
   prodId = udev->descriptor.idProduct;

#if !defined (__LITTLE_ENDIAN)
   /* Some host controller drivers on big endian systems do not swap the usb descriptors */
   if (udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM) || udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM2))
   {
      //Info(plucam, TRACE_PROBE, "USB host controller driver did not swap the 16 bits fields of the USB descriptors\n");
      revId = SWAP16(revId);
      prodId = SWAP16(prodId);
   }
#endif

   if ( (prodId != 0x9a && prodId != 0xa2 && prodId != 0xb2 && prodId != 0x7a)
       || (revId >= 0x10) )  
   {
      //Info(pExt, TRACE_INFO, "*CustomSoftFormatAdjustColorId: skipping\n");
      return 0;
   }

   if (stillMode)
   {
      binY = HI_WORD(pExt->cam_registers.st_subsampling.value);
      colorId = pExt->cam_registers.st_color_id.value;
   }
   else
   {
      binY = HI_WORD(pExt->cam_registers.fo_subsampling.value);
      colorId = pExt->cam_registers.fo_color_id.value;
   }
   useBinY = ((binY & 0x8000)!=0);
   binY &= 0xff;
   
   sixteenBitsMode = ((colorId & 0x04) != 0);

   if ((binY & 1) == 0 && useBinY)
   {
      __u32 newId;

      newId = (sixteenBitsMode ? LUCAM_COLOR_MONO16: LUCAM_COLOR_MONO8);
      if (stillMode)
      {
         pExt->cam_registers.st_color_id.value = newId;
      }
      else
      {
         pExt->cam_registers.fo_color_id.value = newId;
      }
   }
   return 0;
}
