int CustomSoftFormatAdjustStartPos(plucam_device pExt, int stillMode)
{
   struct usb_device *udev;
   int revId, prodId;
   WORD startY;

   udev = pExt -> udev;
   if(!udev) return -EFAULT;

   revId = udev->descriptor.bcdDevice;
   prodId = udev->descriptor.idProduct;

#if !defined (__LITTLE_ENDIAN)
   /* Some host controller drivers on big endian systems do not swap the usb descriptors */
   if (udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM) || udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM2))
   {
      //Info(plucam, TRACE_PROBE, "USB host controller driver did not swap the 16 bits fields of the USB descriptors\n");
      revId = SWAP16(revId);
      prodId = SWAP16(prodId);
   }
#endif

   // Not used, but is a minor feature
   if ( (prodId != 0x9a && prodId != 0xa2 && prodId != 0xb2 && prodId != 0x7a)
       || (revId >= 0x10) )
   {
      //Info(pExt, TRACE_INFO, "*CustomSoftFormatAdjustStartPos: skipping\n");
      return 0;
   }

   if (stillMode)
   {
      startY = HI_WORD(pExt->cam_registers.st_position.value);
      startY &= ~0xf;
      if (startY) startY += 2;
      pExt->cam_registers.st_position.value &= 0xffff;
      pExt->cam_registers.st_position.value |= ((__u32)startY << 16);
   }
   else
   {
      startY = HI_WORD(pExt->cam_registers.fo_position.value);
      startY &= ~0xf;
      if (startY) startY += 2;
      pExt->cam_registers.fo_position.value &= 0xffff;
      pExt->cam_registers.fo_position.value |= ((__u32)startY << 16);
   }
   return 0;
}
