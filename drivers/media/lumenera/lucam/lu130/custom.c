int CustomSoftFormatValidate(plucam_device pExt, int stillMode, unsigned int colorid, unsigned int width ,unsigned int height)
{
   struct usb_device *udev;
   int revId, prodId;
   unsigned int binX, binY;
   int useBinX, useBinY;
   
   udev = pExt -> udev;
   if(!udev) return -EFAULT;

   revId = udev->descriptor.bcdDevice;
   prodId = udev->descriptor.idProduct;

#if !defined (__LITTLE_ENDIAN)
   /* Some host controller drivers on big endian systems do not swap the usb descriptors */
   if (udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM) || udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM2))
   {
      //Info(plucam, TRACE_PROBE, "USB host controller driver did not swap the 16 bits fields of the USB descriptors\n");
      revId = SWAP16(revId);
      prodId = SWAP16(prodId);
   }
#endif

   if ( (prodId != 0x9a && prodId != 0xa2 && prodId != 0xb2 && prodId != 0x7a)
       || (revId >= 0x10) )
   {
      //Info(pExt, TRACE_INFO, "*CustomSoftFormatValidate: skipping\n");
      return 0;
   }

   binX = LO_WORD(pExt->cam_registers.fo_subsampling.value);
   binY = HI_WORD(pExt->cam_registers.fo_subsampling.value);
   useBinX = ((binX & 0x8000)!=0);
   useBinY = ((binY & 0x8000)!=0);
   binX &= 0xff;
   binY &= 0xff;
   
   //Info(pExt, TRACE_INFO, "CustomSoftFormatValidate: width:%d, height:%d\n", width, height);

   // We want to fails a few more cases than the standard driver does

   // Fail cases where height and binningy are incompatible
   if (binY == 2 && height != 1040) return -EINVAL;
   if (binY == 3 && height != 1032) return -EINVAL;
   if (binY == 4 && height != 1024) return -EINVAL;

   // Fail subsampled by 4 in still mode
   if (stillMode && binY == 4 && useBinY == 0) return -EINVAL;

   // Fail subsampling by more than 4
   if (binY > 4 || binX > 4) return -EINVAL;

   // No more checks: success
   return 0;
}
