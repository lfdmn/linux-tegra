int CustomInitReset(plucam_device pExt)
{
   __u16 reg;
   int status;
   __u16 value;
   __u16 index;
   __u16 length;
   BYTE data[4];
   BYTE val;
   __u32 i;
   struct usb_device *udev;
   int revId, prodId;

   udev = pExt -> udev;
   if(!udev) return -EFAULT;

   revId = udev->descriptor.bcdDevice;
   prodId = udev->descriptor.idProduct;

#if !defined (__LITTLE_ENDIAN)
   /* Some host controller drivers on big endian systems do not swap the usb descriptors */
   if (udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM) || udev->descriptor.idVendor == SWAP16(VID_HARDWARE_LUCAM2))
   {
      //Info(plucam, TRACE_PROBE, "USB host controller driver did not swap the 16 bits fields of the USB descriptors\n");
      revId = SWAP16(revId);
      prodId = SWAP16(prodId);
   }
#endif

   //LUCAM_KdPrint(MAX_TRACE, (LUCAM_HEADER"CustomInitReset\n"));

   if ( (prodId != 0x9a && prodId != 0xa2 && prodId != 0xb2 && prodId != 0x7a)
       || (revId >= 0x10) )     
   {
      //LUCAM_KdPrint(MAX_TRACE, (LUCAM_HEADER"CustomInitReset: skipping\n"));
      return 0;
   }

   index = 6;
   length = 4;
   for (i = 0 ; i < sizeof(InitializationMacros) ; i += 2)
   {
      reg = ((__u16)InitializationMacros[i] << 2) | ((__u16)InitializationMacros[i+1] >> 6);
      val = InitializationMacros[i+1] & 0x3f;

      value = (__u16)reg;
      data[0] = val;

      status = lucam_ext_cmd_rw(pExt, index, value, data, length, USB_DIR_OUT);
      if (status < 0) break;
   }

   return status;
}
